import Foundation
import SwiftUI

extension Color {
    public static var CustomGrayColor: Color {
        return Color(uiColor: UIColor(named: "CustomGrayColor")!)
    }
    public static var CustomBackgroundColor: Color {
        return Color(uiColor: UIColor(named: "BackgroundTheme")!)
    }
}
